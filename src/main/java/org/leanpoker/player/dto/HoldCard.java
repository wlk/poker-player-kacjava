package org.leanpoker.player.dto;

public class HoldCard {

    private String rank;
    private String hearts;

    public String getRank() {
        return rank;
    }
    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getHearts() {
        return hearts;
    }
    public void setHearts(String hearts) {
        this.hearts = hearts;
    }
}
